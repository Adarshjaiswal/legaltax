              <div class="overview_sr " id="7">
                <h3>
                  The Procedure of Conversion of a Partnership Firm to
                  Private Limited Company</h3>

                <img src="img/The-Procedure-Of0Conversion-Of-A-Partnership-Firm-To-Private-Limited-Company.png" alt="The-Procedure-Of0Conversion-Of-A-Partnership-Firm-To-Private-Limited-Company">

                <ul>
                  <p>The steps required to convert a Partnership Firm into a Private Limited
                    Company are:</p>
                  <b>
                    <p>Step 1- Conducting a meeting of partners to convert the partnership firm
                      into a private limited company
                    </p>
                  </b>
                  <p>Consent of majority of the partners, at least 3/4 of the partners must be present in
                    person.
                  </p>
                  <p>To authorize two or more partners to take all steps required and execute the conversion
                    process along with the documentation.
                  </p>
                  <p>Consent of the secured creditors - Before conversion, the partners must obtain written
                    consent from the secured creditors of the firm, if any.</p>
                  <p>Apply For DSC And DIN For All proposed Directors and shareholders of the company- it
                    is one of the prerequisites to apply for DSC and DIN of the proposed directors and
                    shareholders.</p>

                  <b>
                    <p>Step- 2 Obtain name Approval in the Run Form
                  </b></p>
                  <p>File a Application in the Run Form on the MCA website to get the incorporation done for
                    the proposed company
                  </p>
                  <p>A partnership firm can apply for the same name, provided the name must be unique as
                    per the rules of the Companies Incorporation Rules 2014 and subject to the availability
                    of the name.
                  </p>
                  <p>The Proposed Director or shareholder shall provide necessary enclosures along with
                    the proposal for conversion of partnership firm.
                  </p>

                  <b>
                    <p>Step- 3 File Form URC-1
                    </P>
                  </b>
                  <p>File a form URC-1 within 30 days of name approval along with the essential documents
                    in the form attachments with ROC
                  </p>

                  <b>
                    <p>Step- 4 Publish An Advertisement in Two Newspaper</P>
                  </b>
                  <li>As per section 374(B) of the Companies Act, 2013 firms opting for incorporation under
                    the provision of Part 1 of chapter XXI shall publish an advertisement about
                    incorporation</li>
                  <li>An advertisement shall be in Form No. URC-2. Further, the advertisement shall be
                    published in 2 newspapers-
                    <ul>
                      <li> 1 in English and,
                      </li>
                      <li> The other is in the principal vernacular language of the district
                      </li>
                    </ul>
                  </li>

                  <b>
                    <p>Step- 5 Draft MOA And AOA</p>
                  </b>
                  <p>Once the name and e- Form URC-1 is approved by the Registrar, the applicant
                    company is required to draft the Memorandum and Articles of Association and other
                    relevant documents required for incorporation.
                  </p>

                  <b>
                    <p>Step- 6 Issue Certificate Of Incorporation
                    </P>
                  </b>
                  <p>File SPICE + along with the required documents and if the Registrar is satisfied with the
                    documents and information filed by the applicants. The Registrar shall issue a COI
                    (Certificate of Incorporation) to the applicant company.
                  </p>
                </ul>
