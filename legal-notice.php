                <h3>
                  What Is a Legal Notice?
                </h3>


                <p>
                  A Legal Notice is basically information or intimation in a clear and unambiguous language that a person or entity sends to another person or entity to do or not to do certain acts failing which the sender of the notice would take legal redress. In simple words, it is a formal communication where you tell the other person that you intend to take legal action against him or her. It is a step before you file a lawsuit. <br><br>

                  The person who sends the notice is called <b>Sender </b> and the person to whom a notice is being sent is called an <b>Addressee</b>. A notice gives the addressee an opportunity to present his side of the story through an amicable method without recourse to the court. A notice is being served only when it is being delivered or being refused to receive by the addressee  <br><br>

                  The issuing of the notice may be a statutory duty or merely optional, but in either case, the purpose of the notice is to inform it’s recipient i.e the addressee precisely and clearly about the intent of the Sender. There must not be only a clear despatch of notice but in order to fix liability on the Sender, there must be clear proof of the notice being received by the Addressee. 
                </p>

