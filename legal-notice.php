                <h3>
                  How to Draft a Legal Notice?
                </h3>
                <p>
                  A Legal Notice can be filed for various issues as mentioned above but if one intends to file a Civil Suit against the Government then that person has to serve a legal notice to the concerned Governmental Organisation before filing a Civil Suit
                </p>


                <p>
                  <b>Section 80 of The Code of Civil Procedure, 1908 </b> provides for sending of a legal notice to the Government or public officer if one wants to initiate any legal action against them for any act committed during their period of employment. The purpose of notice, in this case, is to give an opportunity to the concerned officer to revaluate his action and provide a justification or offer compensation. 
                </p>


                <p>
                  <b> The Apex Court in its landmark judgment of Bihari Chowdhary & Anr vs State of Bihar & Ors</b> has said that the object of the section is the advancement of justice and the securing of public good by avoidance of unnecessary litigation.
                </p>

                <h3>
                  Steps to drafting a Legal Notice are:
                </h3>

                <p>
                  <b>STEP 1 - Contacting a Lawyer: </b>  A legal Notice is being sent through a Lawyer and the first step is reaching out to one.
                </p>

                <p>
                  <b>STEP 2 - Sharing Information: </b>
                  All the information is explained in detail to the Lawyer, like the name of the parties, addresses, issues, grievances, and the relief sought. 
                </p>

                <p>
                  <b>
                    STEP 3 - Providing the Documents:
                  </b>
                  All the necessary and required documents are provided to the lawyer for filing of the notice. 
                </p>

                <p>
                  <b>STEP 4 - Drafting: </b>
                  The notice is then drafted in the Legal Language by the Lawyer clearly mentioning why the notice is being sent, information about previous communications, and a stipulated time e.g 15 days, 30 days or 60 days is given to the addressee to reply. 
                </p>

                <p>
                  <b>STEP 5 - Signing:  </b>
                  The notice is duly signed by the Sender and the Lawyer and is generally sent via Registered post or Courier with the acknowledgment receipt being retained safely. A copy of the notice is also kept by the lawyer for future reference. 
                </p>



