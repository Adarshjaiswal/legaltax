                <h3>What is Locarno Classification Under the Design Act, 2000 ?</h3>
                <p>
                  As per the Locarno Agreement, 2000 designs are registered in different classes. The
                  agreement classifies goods for the purposes of the registration of industrial designs,
                  which further helps in design searches. These classes are mainly function-oriented.
                  Earlier, as per Designs Act, 1911, the classification of design was made on the basis of
                  the material of which the article was made, whereas the new act classifies on the basis
                  of the subject matter of the design i.e., the functionality. The new act has adopted an
                  international classification system, the Locarno Classification System, which is
                  established by the Locarno Agreement. While India is not a signatory to the Locarno
                  Agreement, its classification of industrial design is a template of the Locarno
                  Classification System.

                </p>
