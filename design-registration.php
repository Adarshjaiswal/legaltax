                </h3>
                <p>
                  Any proprietor (design owner) who is seeking registration of a design that is original
                  and has not been published previously in any country and the one which does not seem
                  to be clashing to any law and order of the country, can file an application for getting
                  the design registered. As per section 2(j), the term ‘proprietor’ has been defined as the
