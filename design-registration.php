                <h3>
                  What is the Procedure of Design Registration in India?
                </h3>

                <p>
                  Procedure of Design Registration in India is dealt by Chapter 2 of the
                  Design Act. Following steps should be followed to get your design
                  registered:
                </p>




                <ul>
                  <li>
                    <p>The first and the initial step is to file an application for registration in the
                      patent office in the prescribed form along with the prescribed fee. It is
                      very important to file complete and correct information so that application
                      is processed faster. The class in which design is to be registered must be
                      specified in the application and the “article'' to which it is to be applied.
                    </p>
                  </li>

                  <li>
                    <p>Image/drawing related to the ‘article’ is an essential aspect of a Design
                      application. So, to avoid any sort of complication or any objections with
                      the filing image, an applicant must follow the following tips mentioned
                      below:
                    </p>
                  </li>
                </ul>

                <ol>
                  <li>
                    <p>Image provided by the applicant for design registration must be
                      clear or we can say clearly seen.
                    </p>
                  </li>

                  <li>
                    <p>Images that are present on the contrasting background must be
                      photographed.</p>
                  </li>

                  <li>
                    <p>The image of the design should be photographed from all angles
                      i.e. front, rear, top and bottom.</p>
                  </li>

                  <li>
                    <p>The photograph which consists of articles that is presented for
                      industrial design should be visible in all the images, and no part
                      of the article should be missing.</p>
                  </li>

                  <li>
                    <p>Now, once the application is filed, further, it is examined by the
                      Controller of Design. A detailed evaluation of the application is
                      conducted by the Design Office. In this formal evaluation, the formal
                      compliances like whether applicant’s identification and particulars are
                      correct or not, whether articles fall in the claimed class or not, are
                      checked.
                    </p>
                  </li>

                  <li>
                    <p>If any objection is found, the applicant will be asked to make
                      necessary amendments to get the design registered and nullify
                      objections. The applicant gets an opportunity to file a written response
                      to raised objections. If the Controller finds the written response
                      satisfactory, the application is accepted. If the Controller has an
                      objection, generally, an opportunity of hearing is given now if the
                      Controller is not convinced that the design gets rejected at the hearing.
                    </p>
                  </li>

                  <li>
                    <p>Once the design is accepted, it is then published in the Official
                      Gazette.</p>
                  </li>

                  <li>
                    <p>The validity of registered design is ten years from the date of
                      application, which can be renewed for another term of 5 years.</p>
                  </li>

                  <li>
                    <p>India is one of the endorsers to the Paris Convention, which lays down
                      basic international principles primarily governing the protection of
                      patents, trademarks, and industrial designs.</p>
                  </li>

                  <li>
                    <p>In the case of design infringement, the owner of the design can claim
                      damages and can also apply for an injunction so that design cannot be
                      used further.
                    </p>
                  </li>
                </ol>
