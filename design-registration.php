                </h3>

                <p>
                  <b>DESIGN APPLICATION:</b> An application shall be filed in Form-1, along with the
                  prescribed fees, stating the full name, address, nationality, name of the article, class
                  number and address for service in India.

                </p>
                <ul>
                  <li>
                    <p>Foreign applicants are also required to give an address for service in India, which
                      could be the address of their Agent in India. In case of foreign applicants, it is
                      mandatory to give an address for service in India.</p>
                  </li>

                  <li>
                    <p>Unless such an address is given, the Office shall not proceed with the application.
                    </p>
                  </li>

                  <li>
                    <p>The class to which the article belongs shall be mentioned correctly in Form-1. Under
                      the Designs Rules, 2001, articles have been classified in the Third Schedule based on
                      Locarno Classification. It may be noted that for registering a design in more than one
                      class, a separate application is required to be filed for each class.
                    </p>
                  </li>

                  <li>
                    <p>The application shall be signed either by the applicant or by his authorised
                      agent/legal representative. In India, only a registered patent agent or a legal
                      practitioner can be appointed as an authorised agent.
                    </p>
                  </li>

                  <li>
                    <p>In case, the applicant has already registered a design in any other class of
                      articles, the fact of such registration along with the registration number shall be
                      mentioned in Form-1.
                    </p>
                  </li>
