<div class="clearfix">&nbsp;</div>
<p>This would depend on the type of case. A straightforward case is cheaper, obviously. However, if the case is complicated, say a divorce case, the lawyers may charge you a bit more. In the latter case, lawyers will charge for their opinion apart from their legal fees.</p>
