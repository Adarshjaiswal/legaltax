<h1 class="heading1">What Is Court Marriage?</h1>

<p>In India, court marriages are solemnized by the Marriage Act of 1954. Under the law, a court marriage can perform between a man and a woman who are over the age of 21 and 18 respectively. The parties can be of Indian or foreign descent. There are other conditions such as; either should not be in an existing marriage with another person and that both parties should be of sound mind.</p>
<p>A court marriage does away with rituals and ceremonies and is simple. It is performed by the Marriage Registrar and upon completion; the participants are given a marriage certificate stating that their union is legal in the eyes of the law.</p>
<p>Court marriages are gaining popularity in India; many couples are opting for it. There are numerous reasons why court marriages are the preferred choice these days. One of the reasons is that couples want to keep their marriage economical and simple. Other reasons are because of inter-caste marriages, or marriages without the blessings of the families. Whatever be the case, court marriage is not simple; there are a lot of procedures involved and costs.</p>
<h1 class="heading1">Steps for court marriage in India?</h1>
<div class="clearfix">&nbsp;</div>
<h6 class="font-weight-bold">Informing The Marriage Officer</h6>

<p>The first step is to inform the marriage officer of the district about your intention to marry. You have to do this by filling up the court marriage application form that can be downloaded from the internet. You have to submit the form 30 days prior to the date of the marriage to the officer of the district where either one resides in.</p>

<h6 class="font-weight-bold">Displaying The Notice</h6>

<p>The officer of the district will display the notice at a prominent place at his office for 30 days. Within this period, anyone can object to the marriage if it is deemed illegal under the Act and the eligibility conditions. If there is no valid objection, the officer and perform the marriage without any hassle.</p>

<h6 class="font-weight-bold">Day Of Marriage</h6>

<p>The bride, groom and three witnesses need to sign a declaring form in the presence of the officer or at a location that is reasonably close to the registrar’s office.</p>

<h6 class="font-weight-bold">Marriage Certificate</h6>

<p>One all the formalities are completed, the marriage party and the witnesses need to sign the marriage certificate. It will be ready within 15 to 30 days.</p>



<h1 class="heading1">What are the documents needed for a court marriage?</h1>
<div class="clearfix">&nbsp;</div>
<p>The following documents are required:</p>
<ul>
<li>  A duly filled prescribed form with the necessary fees.</li>
<li>Four passports photographs of the marrying party.</li>
<li>Date of Birth Proof ( This could be your Voting or Pan Card, Municipal Corporation Certificate or X the or XII th Examination Certificate)</li>
<li>Residential Proof ( PAN or Aadhar Card)</li>
</ul>
<p>In case either party is a divorcee or a widow the following document is required:-</p>
<p>A divorce decree or death certificate, whichever is applicable.</p>


