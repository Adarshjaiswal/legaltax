                <h3>What is the procedure accompanied for Change in Share Capital?</h3>
                <p><b>The Procedure Followed For The Change In Share Capital In Brief Is Follows:</b></p>
                <ul>
                  <li>Issue a Board notice with the agenda of the meeting at least seven days before the meeting date.</li>
                  <li>Hold a Board Meeting</li>
                  <li>Pass the Resolution for the Change in Share Capital in the Board meeting.</li>
                  <li>The Resolution passed is subject to the consent of the Shareholders Meeting.</li>
                  <li>For holding a Shareholders Meeting, fix the date, time, and venue for the meeting.</li>
                  <li>Director is allowed to send notice of the Shareholders meeting to the Shareholders of the Company.</li>
                  <li>The announcement for a Shareholders meeting should be published at least before 21 days of the meeting</li>
                  <li>Hold a Shareholders meeting</li>
                  <li>Pass the Resolution with the permission of the majority shareholders.</li>
                  <li><p>Following the passing of the Resolution, Registrar of the Companies (RoC) should get notified about the Change in Share Capital within 30 days of the passing of Resolution. If the Registrar is not implicated about the Alteration within 30 days, then the company or its officers will be responsible for paying a fine of up to 10,000 Rupees for each day of suspension and can be extended to 5 lakh Rupees.</p></li>
                 <br>
                 <p>The above forms should get filed with the designated fees by the Ministry of Corporate Affairs. If the Forms discussed above are not registered with the (ROC) Registrar of Companies, in those case (Section 117 of the Companies Act, 2013) the company must be liable for a fine which shall not fall below five lakh rupees and may reach up to 25 lakhs of rupees. Every officer who is in default will be liable to pay 1 lakh rupee and which can be increased to 5 lakhs of Rupees.</p>
