              <div class="overview_sr " id="3">
                <h3>
                HOW CAN A COMPANY INCREASE THE AUTHORIZED SHARE CAPITAL?
                </h3>
                <h3>AOA Verification of the Company</h3>
                <p>It is mandatory to verify the AOA to ensure that there is a provision in the Articles of Association referring to the increase of the authorized share capital before starting with the procedures for increasing the authorized share capital. If there is no such provision, then the company must first make changes to the company’s AOA.</p>
                <p><b>Note :</b> Most of the AOA’s have the provision for increasing the authorized share capital of the company</p>
                
                <h3>Convene a board meeting</h3>
                <p>It is mandatory to convene a Board meeting by giving notice to Director to increase the authorized share capital of the company. For increasing the authorized share capital, at the board meeting, it is necessary to obtain the approval from the Board of Directors.</p>
                
                <p>After this procedure, for the approval of the shareholders for increasing the authorized share capital and make changes to the MOA of the Company, a date should be fixed to conduct an extra-ordinary General meeting. Basing the approval, the notice of extraordinary general meeting should be presented to all the shareholders, directors, and auditors of the company.</p>
                <p>At last get the approval of the Board of Directors, the company secretary who is present at the meeting to present the notice of the general meeting to the shareholders.</p>
                
                <h3>Extra-Ordinary General Meeting</h3>
                <p>Conduction of the extraordinary general meeting which to leads to the approval of the shareholders to increase the authorized shared capital on time, date and place that is mentioned on the notice.</p>
                <p>The approval gained from the shareholders to increase the authorized capital must be in the form of an ordinary resolution.</p>                
                
                <h3>Filing of ROC Forms</h3>
                <p>At the extraordinary general meeting after the pass of an ordinary resolution a Form SH7 should be filed by the company within 30 days of passing the ordinary resolution. The prescribed government fee for the authorized capital must be paid and the documents that must be attached are mentioned below.</p>
<ol>
  <li>Authorized True Copy of the ordinary resolution.</li>
<li>Notice regarding the Extraordinary General Meeting.</li>
<li>Changed Memorandum of Association (depicting the higher authorized capital)</li>
</ol>
<p>If the mentioned procedure in the Companies Act and the Companies Rules are followed to increase the authorized capital of the company then the approval of the filing and increase in the authorized share capital of the company is approved by the registrar. On MCA portal the new authorized share capital will be reflected.</p>
                <h3>Shares allotment</h3>
                <p>Once the authorized share capital is increased the paid-up share capital of the company can be increased by issuing the fresh equity shares.</p>
   
