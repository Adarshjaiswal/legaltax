                    court in the respective area and ask for the legal heir certificate application.
                  </li>
                  <li>
                    <b>Step 2: Receive the application
                    </b> <br>
                    The applicant has to obtain the application form from the concerned officer.
                  </li>
                  <li>
                    <b>Step 3: Enter the Details</b> <br>
                    This application contains the names of all the legal heirs, their relationship with the
                    deceased, and the family members’ addresses.




                  </li>
                  <li>
                    <b>Step 4: Attach the Documents</b> <br>
                    All the required documents, including the departed person’s death certificate, should be
                    attached to the application. (Death certificate must be obtained from the
                    municipality/corporation office).

                  </li>
                  <li>
                    <b>Step 5: Submit the application</b> <br>
                    The applicant has to submit the application form with an affidavit on stamp paper or
                    self-declaration must be submitted along with the application to the concerned officer.

                  </li>
                  <li>
                    <b>Step 6: Verification Process
                    </b> <br>
                    On submitting the application, the local revenue officers and village administrative
                    officials will verify the application.
                  </li>
                  <li>
                    <b>Step 7: Issuing the certificate </b> <br>
                    Once the successful enquiry, the authorized officer issues the Legal heir certificate. The
                    process of obtaining a legal heir certificate generally takes 30 days.
                  </li>

                </ol>

