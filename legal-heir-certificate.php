                <p id="h_ltx">
                  Procedure to Obtain a Legal Heir Certificate
                </p>
                <p>
                  This certificate can be applied only by the legal heirs after obtaining the death certificate
                  of deceased member of the family . The legal heir certificate can be obtained by
                  approaching the sub divisional magistrate court or from the corporation/municipality
                  office of the respective area and the district civil court. This certificate is issued only
                  after a proper enquiry of all legal heir’s and deceased persons . The process of
                  obtaining a legal heir certificate is stated below
                </p>
                <ol>
                  <li>
                    <b>
                      Step 1: Submit an application form in the sub- divisional magistrate office/district
                      court/MCD office
                    </b> <br>
