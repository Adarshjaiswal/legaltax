                </h3>
                <img src="img/PROCESS-OF-FIRST-TIME-FILING-DIRECTOR-KYC.png" alt="PROCESS-OF-FIRST-TIME-FILING-DIRECTOR-KYC">
                <ul>
                  <p>If for the first time the director is filing DIN KYC, it has to be filed by uploading a duly filled copy of the MCA Prescribed e form DIR-3 (KYC). The copy of address proof must be filed as a mandatory attachment along with the e form. The e-form DIR-3 (KYC) must be signed with the applicant director’s digital signature, followed by attestation by a CS, CMA, or CA in full time practice. The main steps for filing the DIR-3 KYC is as under:
                  </p>
                  <ol>
                    <li>
                      <p><b>Checking the DIN Status at MCA: </b>At the MCA website check the status of DIN.</p>
                    </li>
                    <li>
                      <p><b>Scan Address Proof of Director: </b>Along with the e-form DIR-3 KYC attachment of self-attested scanned copy of the address proof of the applicant must be done. Mobile Bill/ Electricity Bill/Telephone Bill/ Water Bill or Bank Statement/Bank Passbook with the latest transaction is considered as an important document for address proof. The address proof should not be older than two months.</p>
                    </li>
                    <li>
                      <p><b>Preparation of DIR-3 Form and Verification of email/Mobile: </b> For the first time KYC filing is done by submission of an e-form to the ROC. The personal email address and mobile number of the director or designated partner are verified by way of an OTP. For every person unique email and mobile number should be used for every person. A similar way of OTP Verification is applicable in the case of web-based KYC Filing.</p>
                    </li>
                    <li>
                      <p><b>Process of filing of DIR-3 KYC e-form to ROC: It </b>It is mandatory that e-form must be digitally signed by the director and thereafter the same is certified by CA, CS or CMA. There will be nil fee prescribed if the DIR-3 (KYC) form is filed within its due date. However, after the due date is over there is an additional fee of Rs. 5000 is attracted for each and every person having DIN.</p>
                    </li>
                  </ol>
                  <h3>WEB-BASED ONLINE DIN KYC</h3>
                  <p>For someone who has filed there DIN KYC in the previous year and in case his particulars, such as mobile number, residential address and email have not changed, then such person are eligible to file the Director KYC in a web based facility with authenticating with OTP. By inserting a provision in Rule 12A of the Companies (Qualification and Appointment of Directors) Rules, 2018, the Ministry of Corporate Affairs has given a facility on the MCA portal to file the director KYC through a

                    web-based filing. In Web-based filing there is no need to submit or upload any document. However, it must be ensured by director that the director’s particulars are correct and there is no as such change in the particulars.
                  </p><br>
                  <h3>NOTE:</h3>
                  <ol>
