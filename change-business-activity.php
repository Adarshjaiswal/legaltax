                <h3>
                  OBJECTIVE OF BUSINESS AND ITS CHANGE
                </h3>
                <p>
                  The business objectives are defined in the main object clause of MoA, when particularly talking about the
                  company. The business objectives define the scope of the company's principal of business activities within
                  which it may operate. A company cannot undertake those activities, which are not mentioned in its object
                  clause. With strategic changes there is a reason of expansion, a company may require to undertake new
                  activities or update business activities in MoA. After the approval from Ministry and consent of
                  shareholders this change in object clause of MoA is possible.

                </p>
