              <div class="overview_sr " id="2">
                <h3>
                  CHANGE IN THE OBJECTIVE OF THE BUSINESS
                </h3>
                <ul>
                  <li>
                    <p>
                      <b> New Activities to be undertaken:</b>
                      the main reason to change business objective is the addition of
                      new activities. The change is also backed by the horizontal or vertical expansion of the activities,
                      which were not mentioned while incorporation of company. Therefore, the company must
                      undertake the change of business activity prior to undertaking such activities.
                    </p>
                  </li>
                  <li>
                    <p>
                      <b> Takeover of the Company:</b>
                      many times a company is takeover by another company just for the
                      sake of its good market value. For leading the business further the objects of the company acquired
                      must be updated. The object can be changed either before or after the takeover depending on the
                      requirement.
                    </p>
                  </li>
                  <li>
                    <p>
                      <b> Removal of Abandoned activities:</b>
                      the memorandum of the company is accessible to any person,
                      who may refer it before binding into contractual relation. Hence, one must make sure that the
                      objects must be updated with the Ministry even by deleting certain activities that the company has
                      abandoned to undertake.
                    </p>
                  </li>
                  <li>
                    <p>
                      <b> Ban or prohibition of Current activities: </b>
                      The Government policies have the probability to
                      change anytime, which essentially affects the company and its operations. If the undertaken
                      activities by a company is declared to be banned or prohibited, such an object must be changed
                      completely or removed from MoA, as required.
                    </p>
                  </li>
                </ul>

