                <h3>
                  Overview
                </h3>

                <p>
                  Limited Liability Partnership (LLP) in India was introduced through the LLP Act, 2008. The basic
                  concept behind adopting LLP was to provide a structure which is easy to maintain and minimizes
                  liability as compared to sole proprietorship structure. LLP combines the benefits of both the company
                  and the partnership firm into a single form of organization and provides a hybrid structure. Hence,
                  converting sole proprietorship into LLP is a good business decision.

                </p>

              </div>

              <div class="overview_sr " id="2">
                <h3>
                  Reasons Why You Should Convert Proprietorship to LLP

                </h3>
                <img src="img/Reasons-Why-You-Should-Convert-Proprietorship-To-LLP.png" alt="Reasons-Why-You-Should-Convert-Proprietorship-To-LLP.png">
                <ul>
                  <li>
                    <p> <strong> Separate Legal Existence. </strong></p>

                    <p> A limited liability partnership is a separate legal entity, and its existence is separate from
                      that of its partners, unlike a general partnership firm. This makes it possible to own the
                      property and enter into a contract in the name of the LLP or sue a third party in case of any
                      dispute. </p>
                  </li>

                  <li>
                    <p> <strong>Limited Liability of Owners </strong> </p>

                    <p>The liability of the partners is limited to the extent of capital contribution agreed by the
                      partners in the LLP agreement. The loss or debt of the LLP cannot be assigned to the partners even
                      during the dissolution of the LLP. In addition, one partner is not held responsible for the acts
                      of negligence or misconduct of another partner.
                    </p>
                  </li>

                  <li>
                    <p> <strong>Flexibility to Operate</strong> </p>

                    <p>The LLP is managed and operated as per the LLP Agreement. It is the partners who decide how the
                      LLP will function and divide the duties and responsibilities. Hence, it is a very flexible
                      structure and the participants are free to make their own rules of management which is not
                      possible in other business structures.</p>
                  </li>

                  <li>
                    <p> <strong>Lower Compliance Requirement</strong> </p>

                    <p>Compared to a private company, LLP has less compliance requirements, including audit
                      requirements. The need for statutory audit arises when a certain level of turnover or contribution
                      is reached. Further provisions like meeting of partners, operation through resolutions have been
                      relaxed and are not mandatory in every case.</p>

                    <p>There are many more reasons to choose an Proprietorship to LLP </p>
                  </li>

                </ul>



