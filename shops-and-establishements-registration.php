                                <h3>
                                    The Procedure for Obtaining Shop and Establishment License Is Discussed Below
                                </h3>

                                <ul>
                                    <li>
                                        <p>
                                            <b> Download the Application Form: </b>
                                            First and foremost step is to download the application form for the registration from the official website of the respective state in which the said shop or establishment is located.
                                        </p>
                                    </li>

                                    <li>
                                        <p>
                                            <b> Fill in all the Required Details: </b>
                                            In the next step, the owner of the business is required to fill all the necessary details asked. The term necessary details include the following listed :
                                        </p>
                                        <ol>
                                            <li>
                                                <p>
                                                    Name of the Proposed Shop and Establishment
                                                </p>
                                            </li>
                                            <li>
                                                <p>
                                                    Name and Details of the concerned employer
                                                </p>
                                            </li>
                                            <li>
                                                <p>
                                                    Name and Details of al the Employees working at that time
                                                </p>
                                            </li>
                                            <li>
                                                <p>
                                                    Address proof of the Registered Establishment together with the NOC (No-Objection Certificate) or the Rent Agreement, only in the case where the place is rented premises.
                                                </p>
                                            </li>
                                            <li>
                                                <p>
                                                    PAN card details of the concerned employer.
                                                </p>
                                            </li>
                                        </ol>
                                    </li>

                                    <li>
                                        <p>
                                            <b> Submit the Application Form: </b>
                                            Once the owner or the employer have fills all the required details accurately, then in the next step he or she needs to submit the said application form either to the Inspector in-charge or the Chief Inspector of the Shop and Establishment Act together with prescribed amount as fees.
                                        </p>
                                    </li>

                                    <li>
                                        <p>
                                            <b>Verification of the Application Submitted: </b>
                                            After the submission, the application along with all the documents submitted will be verified and checked by the higher authorities.
                                        </p>
                                    </li>

                                    <li>
                                        <p>
                                            <b>Issuance of License: </b>
                                            If in case, all the documents submitted along with the applications are in accordance to the requirements of the said act, then a certificate of registration shall be issued to the proposed shop or establishment.
                                        </p>
                                    </li>
                                </ul>
