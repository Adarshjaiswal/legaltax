<h6>5 TYPES OF GOVERNMENT TENDERS ON GEM?<br>
</h6>
<h5><strong>i. Advertised Tender Enquiry</strong></h5>
<p>The government advertises requirement on GeM portal and Central Public Procurement Portal (CPPP) for procurement of goods and services. And give Invitation to sellers to apply tenders.</p>
<p>This method used by government organizations when the procurement of goods and services of the value of INR. 25 lakhs and above.</p>
<h5><strong>ii. Limited Tender Enquiry</strong></h5>
<p>This method is used when any goods and services regularly procured by government departments such as stationery, printer cartridge filling, water supply, etc.</p>
<p>Under limited tender inquiries, limited suppliers are impanelled by the Government Department.</p>
<p>Usually, Limited Tender Enquiry is used when the estimated value of the goods and services to be procured is less than INR 25 Lac.</p>
<h5><strong>iii. Two-Stage Bidding</strong></h5>
<p>This is a traditional method used to procure high-value items such as purchasing plant and machinery etc. and when any procurement requires to assess complex and technical specifications.</p>
<p>Under Two-Stage Bidding, tender is allocated to a supplier based on successfully passing below 2 stages:</p>
<ul>
<li>Technical Assessment,</li>
<li>Financial L1 (Lowest Price) Bid</li>
</ul>
<h5><strong>iv. Single Tender Enquiry</strong></h5>
<p>This method adopted when the availability of desired goods and services scarce in the open market or suppliers are very limited.</p>
<p>This method is very useful for startups having unique and innovative products. In order to empower startups, the government has also launched a special scheme “Startup Runway” for procuring unique and innovative products directly from startups.</p>
<p>There can be several circumstances such as</p>
<ul>
<li>Only a single supplier is available to the best of the knowledge of the government department’s officials.</li>
<li>When the procurement of goods is urgent and necessary to purchase from a particular known source.</li>
<li>When specific supplementary machines or spare parts required which is only compatible with existing procured machinery.</li>
</ul>
<h5><strong>v. Electronic Reverse Auctions</strong></h5>
<p>Auction is what when you give a bigger bid to buy the auctioned item. Under Reverse auction, you bid a lower price to sell your product.</p>
<p>An Electronic Reverse Auction is a type of online auction available on GeM portal. Under electronic reverse auction, there is one buyer (government department) and many potential sellers (private companies). The sellers give lower bid to obtain business from the buyer and bid will typically decrease as the sellers underbid each other.</p>
