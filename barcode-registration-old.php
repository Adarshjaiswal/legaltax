<li>EAN/UPC Barcodes:- EAN/UPC Barcodes are suitable for the retail point of sale (POS)because they are designed for the high volume scanning environment.</li>
<li>GS1 DataBar Barcodes:- DataBar barcodes are used on coupons and to label fresh foods.These barcode can hols information like an item’s batch number or expiry data.DataBar barcodes are often used in retail and can be read by laser scanners.</li>
<li>One-dimensional barcodes:- GS1-128 &amp; ITF-14 are highly versatile 1D barcodes that enables items to tracked through global supply chains. Basically it is used in general distribution and logistic.</li>
<li style="text-align: justify;">Two-dimensional barcodes:- A 2D barcode can hold a significant amount of information.It is used in a wide range of industries,from manufacturing and warehousing to logistic and healthcare.</li>
</ol>
