                    Step1: Filing of Application:
                  </b>
                  The application for Trademark Renewal in India is made in Form TM-12. The application is required to be made by the owner of the registered Trademark or by an authorized agent of the owner. The help of the professional is advised to be taken while filing the application of the Trademark Renewal in India. The guidance from the professionals will ensure that the Trademark is very well protected.
                </p>


                <p>
                  <b>
                    Step2: Status Check of Application:
                  </b>

                  After filing of the application for Trademark Renewal, it is very important to check for the status of the filed application from time to time until the Registrar of Trademark processes the same. The Trademark Renewal in India, from time to time, requires various time bound responses or actions from the applicant of the registered Trademark.
                </p>


                <p>
                  <b>
                    Step3: Advertisement in Trademark Journal:
                  </b>

                  The Trademark journal is an Official Gazette in the Trademarks Registry, which states that whatever the application filed for registration of Trademark is accepted or not by the registry. If the Registrar of Trademark thinks that the application filed is acceptable, then the Trademark is officially advertised in the Journal of Trademark at the Registry. When the application filed is advertised in the Trademark Journal at the Registry, the third party is given an opportunity for the opposition of the Registration of Trademark. The process of Trademark Opposition is not needed while there is a Renewal of an already registered and existing Trademark in India.
