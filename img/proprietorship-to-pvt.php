                  <li>The sole proprietor must complete all the steps related to the slump sale formalities.</li>
                  <li>It is mandatory for the sole proprietor to obtain DIN and DSC for all the persons who are going to be the directors of the company to be incorporated.</li>
                  <li>For checking the availability of name for the new private company application has to be made.</li>
                  <li>Drafting of the MOA and AOA of the new Private Limited Company must be done by the sole proprietor. In MOA, there must be an addition of objective stating that the sole proprietorship has been taken over by the company.</li>
                  <li>Online application must be filed by the sole proprietor for Company Registration from the Ministry of Corporate Affairs online portal.</li>
                  <li>Along with the application form all the documents should be submitted.</li>
                  <li>The Certificate of Incorporation from the Registrar of Companies must be secure by the applicant.</li>
                  <li>The applicant should then apply for TAN and PAN numbers from the authorized signatory.</li>
                  <li>At last, updating the bank accounts of the private limited company for carrying out transactions.</li>
                </ul>
