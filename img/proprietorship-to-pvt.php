              <div class="overview_sr " id="5">
                <h3>
                  What are the Benefits of Conversion of Sole Proprietorship Into Private Company?
                </h3>
                <p>There are a lot of benefits related to A Private Limited Company, such as:</p>
                <ul>
                  <li>For the sole proprietorship there is no formal registration, Under the Companies Act 2013, the Private limited companies are registered.</li>
                  <li>There is no separate legal entity that hold the sole proprietorship, and the private limited company is a separate legal entity.</li>
                  <li>In case of sole proprietorship, there is no transfer of shares, whereas, in case of private limited company, the shares are transferred easily.</li>
                  <li>The way sole proprietor raise the fund or capital for expansion just the same way a Private Limited Company can raise the fund or capital for expansion.</li>
                  <li>In a Private Limited Company, in case of losses, liabilities are limited by shares or warranty, whereas the sole proprietor is not a corporate entity, are not able to enjoy such benefits.</li>
                  <li>A sole proprietor is not been able to afford capable and highly qualified employees, whereas for Private Limited Company it is much easier.</li>
                  <li>There is unlimited liability in case of sole proprietorship, but it is limited to the shares of private limited company.</li>
                  <li>Unlike sole proprietorship, A Private Limited Company is more genuine because, it is registered and authenticity of the business.</li>
                </ul>

              </div>
              <div class="overview_sr " id="6">
                <h3>What is the Procedure for Conversion of Sole Proprietorship to Private Limited Company?</h3>
                <p>The conditions that must be followed before converting a sole proprietorship to a private limited company:</p>
                <ul>
                  <li>After incorporation of a new private limited company, there is transfer of all the assets and liabilities of the old sole proprietorship completely to the incorporating company.</li>
                  <li>The old sole proprietorship will hold 50% share in a new private limited company i.e., 50% of the voting rights will be held by a sole proprietor, even after the conversion takes place.</li>
                  <li>From the date of Incorporation of a new private limited company the old sole proprietor will hold shares for a minimum period of 5 years.</li>
                  <li>There will be not any monetary consideration between a private limited company and a sole proprietor as it is a mere conversion, not a sale.</li>
                </ul>
              </div>








