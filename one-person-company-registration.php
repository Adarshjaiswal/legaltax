                <h3>
                  Process For One Person Company Incorporation
                </h3>
                <img src="img/doc_opc_process.png" alt="doc_opc_process">

                <ul>
                  <li>
                    <p>
                      <b> Step 1: Apply for DSC and DIN : </b>
                      The first step of the OPC registration is to get DSC or Digital Signature Certificate of the Directors. For this some documents are needed these are as followes:
                    </p>
                    <ol>
                      <li>
                        <b> Aadhar card of the proposed director</b>
                      </li>
                      <li>
                        <b> Address proof of the proposed director</b>
                      </li>
                      <li>
                        <b>Email id and Phone number of the proposed director</b>
                      </li>
                      <li>
                        <b> Latest photo of the director</b>
                      </li>
                      <li>
                        <b>PAN card of the director</b>
                      </li>
                    </ol>
                    <p>
                      Once the DSC is made, now the next step will be to get the Director Identification Number (DIN) of the proposed director in SPICe Form <b>(Simplified Performa for Incorporating a Company Electronically)</b> along with the address and identity proof of the Company’s director.
                    </p>
                    <br>
                    <p>
                      Form DIR-3 is the option only available for present companies. The candidate does not need to file the Form DIR-3 separately. Now the Director Indentification Number can be applied within SPICe Form for up to 3 directors
                    </p>
                  </li>
                  <li>
                    <p>
                      <b>
                        Step 2: Application for Name Approval:
                      </b>
                      After obtaining <b>DSC</b> and <b>DIN</b>, the next step is to decide the company's name. The company's name will be in the form of <b> XYX (OPC) </b> Private Limited. The name can be approved in the <b>SPICe and 32 Form.</b> Only one preferred name and the importance of keeping that name can be given in the <b>SPICe and 24 Application Form.</b> In case the name of the company is not accepted or get rejected, another name can be submitted by applying another SPICe and 32 Form. Once the suggested name is approved by the <b><a href="https://www.mca.gov.in/MinistryV2/applyfordin.html" style="color: blue;"> Ministry of Corporate Affairs (MCA)</a> </b>, you can move to the next step.
                    </p>
                  </li>
                  <li>
                    <p>
                      <b> Step 3: Essential Documents: </b> After the approval of the name, the documents which are required must be submitted to the registrar of companies mentioned bellows:
                    </p>
                    <ol>
                      <li>
                        <p>
                          <b> Memorandum of Association (MOA) </b> must be submitted in which the objects to be followed by the proposed business or company.
                        </p>
                      </li>

                      <li>
                        <p>
                          <b> Articles of Association (AOA)</b> must be submitted which lays down the laws on which the company will operate.
                        </p>
                      </li>

                      <li>
                        <p>
                          Since there is only one member, or director, a nominee on behalf of such individual has to be assigned because in case he or she becomes injured or dies and cannot perform their duties, the nominee will perform all of his or her duties on behalf of the director and take his or her place. Their approval in <b>Form INC-3</b> will be taken along with their PAN Card and Aadhar card.
                        </p>
                      </li>

                      <li>
                        <p>
                          Approval as well as declaration of the proposed Director of Form <b>DIR-2 and INC-9</b> respectively.
                        </p>
                      </li>

                      <li>
                        <p>
                          Submission of the proof of the registered premise or office of the proposed company along with the ownership proof and a No Objection Certificate from the owner.
                        </p>
                      </li>

                      <li>
                        <p>
                          Submission of a declaration by the expert certifying that all compliances have been made.
                        </p>
                      </li>
                    </ol>
                  </li>
                  <li>
                    <p>
                      <b> Step 4: Forms Filing with MCA: </b> all such documents will be attached to the <b> SPICe +Form, SPICe-AOA, and SPICe-MOA</b> with the Digital Signature Certificate of an expert and the director and will upload the same on the official website of MCA for approval. There is no need to file separate applications for getting <b> TAN and PAN numbers.</b>
                    </p>
                  </li>
                  <li>
                    <p>
                      <b> Step 5: Issuance of the Registration Certificate: </b> After verification of all the forms and documents, the ROC (Registrar of Companies) will issue a Registration Certificate and you can start a business.
                    </p>
                  </li>
                </ul>
