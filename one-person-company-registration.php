                <h3>
                  Benefits Of Opc Registration In India
                </h3>
                <ol>
                  <li>
                    <p>
                      <b>Benefits Of Opc Registration In India</b> for the registration of One Person Company there is no minimum capital is required. However, the maximum authorised capital in case of one person company shall not anyhow exceed the thrush hold limit of Rs. 50 lakh at any point of time.
                    </p>
                  </li>

                  <li>
                    <p>
                      <b> Limited liability: </b>
                      another significant benefit connected with the concept of registration of One Person Company is of limited liability. This means that the liability of the concerned directors is limited to the extent of capital contributed by him or her in the business. Hence, the personal asset and belongings of the Director will not be attached in case of nay loss incurred by the business.
                    </p>
                  </li>

                  <li>
                    <p>
                      <b> Fewer Compliances: </b> The compliances that are to be adhered to for an OPC registration are very less in comparison to any other company. Hence, the registration of an OPC can be done easily that too with minimum paperwork.
                    </p>
                  </li>
                  <li>
                    <p>
                      <b> Perpetual Succession: </b> The term perpetual succession means that the death or illness or the incapacity of the director will not affect the ongoing affairs of the company as the nominee will hold the position of member and director in the business in that case.
                    </p>
                  </li>
                  <li>
                    <p>
                      <b> Greater Credibility: </b> One Person Company is obligated to get its books of account audited annually. This will, in return, increase the business credibility and consumer, vendor satisfaction.
                    </p>
                  </li>
                  <li>
                    <p>
                      <b> No legal Disputes:</b> It is significant to note that whenever a company registers itself as a One Person Company, it ends the chances of any future legal disputes between the director or any third party.
                    </p>
                  </li>
                  <li>
                    <p>
                      <b> Privileges for small scale industries: </b> An OPC can avail all the benefits that are offered to the small scale industries. These benefits include easy funding that too without depositing collateral security to certain prescribed limits, lower interests loans, privileges under the foreign trade policy etc. Therefore, these benefits play a significant role in the progress and development of the One Person Company in its initials days of incorporation.
                    </p>
