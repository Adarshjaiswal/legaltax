                </h3>
                <p>
                  <b>An Overview</b>
                </p>
                <p>
                  A One Person Company is a company that has only one person as a member. One Person Company (OPC) was came into effect to encourage individuals who are a potential holders for starting their own business. OPC enables a sole proprietor to convert his firm into a Limited Liability company and also avails the benefit related to the Company. <br><br>
                  OPC is a kind of a business structure that enjoys the benefits of both form of business i.e. a sole proprietorship and a company. It also eliminates the complications related to finding the right kind of co-partners for starting a business as registered entity.
                  <br><br>

                  As Per Section 2(62) of the Companies Act, 2013, One Person Company means a company that has only one person as a member. One Person Company is bringing the unstructured Proprietorship Business into the structured version of a private company. OPC is opening the path for sole proprietors and start-ups.
                  <br><br>
                  One Person Company feature is such that is has only one share holder who owns 100% stake of the company. To understand one person company definition, we first need to get into the identity it creates. A registration not only provides corporate status but it also provides many benefits to the members and the directors. In case of Private Company, at least two members are required which is not the same in the case of OPC. To eliminate this drawback and allow a single person to get the advantages of One Person Company, this sort of a company structure is introduced through the Companies Act, 2013. One Person Company registration is simplified with online filing and process. <br><br>
                  The structure of the one person company (OPC) in recent times was launched as a refinement of the structure of a sole proprietorship firm. In an OPC, a single promoter gains full authority over the company thereby, restricting his/her liability towards their contributions to the enterprise. Therefore, the said person will be the sole shareholder and director (however, a director nominee is present, but has zero power until the real director proves incapable of carrying on). Also, there can be no opportunity for contributing to employee stock options or equity funding. Additionally, if an OPC has an average turnover of ₹2 crores thrice in a row and over or acquires a paid-up fund of ₹50 lakh and over, it has to be converted to a private limited company or public limited company within six months.


