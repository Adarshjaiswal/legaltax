                                <h3>
                                    Effects of Partnership to LLP Conversion
                                </h3>

                                <ul>
                                    <li>
                                        <p>An LLP can use a name other than listed in the registration certificate
                                        </p>
                                    </li>
                                    <li>
                                        <p>All finances, privileges and rights vested in the firm should be transferred
                                            to the LLP.
                                        </p>
                                    </li>
                                    <li>
                                        <p>The firm shall cease to operate under the Indian Partnership Act 1932. Its
                                            credibility will be removed from the record.
                                        </p>
                                    </li>
                                    <li>
                                        <p>The pending cases will be transferred to LLP after completing the process
                                            of Partnership to LLP conversion. The same is true for past judgments or
                                            orders in favor of or against the firm.</p>
                                    </li>
                                    <li>
                                        <p>All existing agreements and contracts shall continue to be effective with the
                                            LLP as party</p>
                                    </li>

                                </ul>

                            </div>




