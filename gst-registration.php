                  <h3>An overview of  New GST Registration</h3>
                  <p>GST is an abbreviation for Goods and Services Tax, which is an indirect tax placed on the supply of goods and services in India. GST was introduced in 2017 as a landmark reform to replace the multiple taxes and cesses that were imposed by the Central and State Governments. GST aims to simplify the tax system, eliminate cascading effects of taxes, widen the tax base, and improve compliance.</p>
                  <p>GST is based on the principle of destination-based taxation, which means that the tax is collected by the State where the goods or services are consumed, rather than where they are produced or supplied. GST is also a value-added tax, which means that the tax is levied only on the value added at each stage of the supply chain, and not on the entire value of the transaction.</p>
