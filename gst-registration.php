                <h4>1.	Regular GST Registration: </h4>
                <p>Most firms that are mandated to register under GST are subject to regular GST registration. This form of registration is appropriate for firms that offer goods or services inside a state or across many states in India. The following are the essential steps in the normal GST registration process:</p>
           
                <ul class="listAlpha">
                  <li><strong>Navigate to the GST online portal:</strong> The applicant must go to the government-designated official GST portal. </li>
                    <li><strong>Complete the Online GST Registration application form as follows:</strong> The applicant must supply pertinent information such as the legal name of the company, address, PAN (Permanent Account Number), bank account information, and contact information. Documents such as the business owner's PAN card, identification, and address proof are also required, and proof of business registration may also be required.</li>
                      <li><strong>Submit your application and get your Application Reference Number (ARN):</strong> An Application Reference Number (ARN) is produced once the application form is completed and submitted. This number can be used to track the application's progress.</li>
                        <li><strong>Verification and processing:</strong> The application is verified by the GST authorities, who examine the given information and documentation. If everything checks out, the GST registration is accepted, and the firm receives a GSTIN (GST Identification Number).</li>
                  
                </ul>

                <h4>2.	Composition Scheme GST Registration: </h4>
                <p>The Composition Scheme is an alternate and streamlined way of GST tax payment. It is intended for small enterprises with low turnover. Businesses that are registered under the Composition Scheme pay less tax and have fewer compliance obligations. Here's how the Composition Scheme's online registration works:</p>
           
                <ul class="listAlpha">
                  <li><strong>Log in to the GST portal:</strong> The applicant must log in to the official GST portal, just like with standard GST registration.</li>
                  <li><strong>Complete the Composition Scheme application:</strong> The applicant must supply the necessary information, such as the legal name of the business, address, PAN, bank account information, and contact information. Documents proving the company owner's identity and address may also be requested.</li>
                  <li><strong>Submit your application and get your Application Reference Number (ARN):</strong> An Application Reference Number (ARN) is generated once the application form is submitted, allowing the applicant to trace the status of the application.</li>
                  <li><strong>Verification and processing: </strong>After the application is verified, the Composition Scheme registration is accepted if everything is in order. The company will be given a GSTIN and will be eligible for the Composition Scheme advantages.</li>
