                    226 of the Constitution of India.. Such petition is filed by using the Writ of Habeas
                    corpus for obtaining Child Custody.. It is not a full-fledged child custody petition but has
                    limited jurisdiction. However, the Supreme Court of India and High Courts of every state
                    of India have authority with extraordinary powers/jurisdiction to determine the issue..
                  </li>
