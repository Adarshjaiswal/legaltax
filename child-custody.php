                      about the child's upbringing and welfare, similar to when they were
                      married (legal custody).It can also be called as physical custody
                      Example: Mother and Father arrange a schedule where Child lives with
                      each parent for one month at a time (physical custody), and they agree
                      that they will decide together on all major issues related to Child's
                      welfare and upbringing (legal custody).

