                      that he /she will be the biological parent or not . This type of custody is
                      only possible if both parents agree on the same. Here is an important
                      difference between legal and physical custody, which you should be
                      familiar with. Legal custody gives power, rights and responsibilities for
                      raising or caring for the child. In contrast, physical custody means where
