                <h3>
                  What Is Section 138 of the Negotiable Instrument Act?
                </h3>

                <p>
                  Section 138 of the Negotiable Instruments Act provides that cheque bounce for the insufficient funds is an offence and punishable with imprisonment. It provides that where a person draws a cheque on an account maintained by him for the payment of money to another person, and it is returned by the bank unpaid because of insufficient funds to honour the cheque, the drawer of the cheque commits an offence. The person must draw the cheque for discharge of any legally enforceable debt or liability. The cheque must be returned by the bank because the amount of money standing to the credit of the drawer’s account is insufficient or exceeds the amount arranged to be paid from the drawer’s account by an agreement made with the bank. <br><br>

                  The drawer can be punished with imprisonment for a term not exceeding two years or a fine that can extend to twice the amount of the cheque, or both for the offence of the cheque bounce.
                </p>


                <h3>
                  What Is Section 144 of the Negotiable Instrument Act?
                </h3>
                <p>
                  Section 144 of the Negotiable Instruments Act provides that where cheque bounce has taken place by the cheque issued by a company, then every person in charge and responsible for the conduct of the business of the company at the time of the offence was committed is guilty of an offence. This section provides that the cheque bounce for insufficient funds is an offence when the drawer of the cheque is a company. <br><br>

                  When the drawer of the bounced cheque is a company, every person in charge and responsible for conducting the business of the company and the company is guilty of the offence of the cheque bounce and can be punished accordingly. <br><br>

                  Where a cheque bounce offence is committed by a company, and it is proved that the offence was committed by the company with the consent, negligence or convenience of the director, secretary, manager or other officers of the company, such persons will be deemed guilty and liable to by punished accordingly.
                </p>






