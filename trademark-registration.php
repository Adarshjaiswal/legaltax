                <h3>Trademark Objections  </h3>
                <p>Trademark objections are raised by the trademark examiner during the examination stage of the registration process. The examiner may object to the trademark application on various grounds, such as lack of distinctiveness, similarity to existing trademarks, descriptiveness, genericness, or violation of public order or morality. The examiner will issue an examination report detailing the grounds of objection and giving the applicant a chance to respond. The applicant may submit arguments and evidence to overcome the objection or amend the application accordingly. If the examiner is satisfied with the response, the application will proceed to publication. If not, the application will be refused.                </p>
              
                <h2>Trademark Opposition</h2>
                <p>Trademark oppositions are filed by third parties after the publication of the trademark application in the official gazette. Any person who believes that they would be adversely affected by the registration of the trademark may file an opposition within a specified period, usually three or four months. The grounds of opposition may vary depending on the jurisdiction, but they typically include prior rights, bad faith, likelihood of confusion, or non-use. The opposition procedure involves a notice of opposition, a counterstatement by the applicant, evidence and arguments by both parties, and a decision by the trademark office or a tribunal. The outcome of the opposition may be the acceptance, rejection, or partial acceptance of the trademark application.</p>
              
              </div>


