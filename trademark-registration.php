                  <li><strong>Trademark Eligibility: </strong>The trademark must be distinctive, meaning that it can distinguish the goods and services of the applicant from those of others. The trademark must not be descriptive, generic, deceptive, scandalous, or contrary to any law or morality. The trademark must also not conflict with any existing registered or pending trademarks.</li>
                  <li><strong>Goods and Services Classification:</strong> The goods and services for which the trademark is sought must be classified according to the Nice Classification system, which divides them into 45 classes. The applicant must specify the class or classes that correspond to their goods and services and provide a clear and concise description of each.</li>
