<h1>What is Employee State Insurance Corporation</h1>
<p style="text-align: justify;">Employee State Insurance Corporation or ESIC is a self-financing social security and health insurance scheme which provides medical benefit, sickness benefit, maternity benefit, disablement benefit and various other benefits such as funeral expenses, free supply of physical aids etc. to the employees and their family.</p>
<p style="text-align: justify;">Units or Establishments that have 10 or more employees, drawing the wages of up to Rs.15,000 a month are required to be registered for ESIC under the ESI Act 1948. The benefits provided by the scheme are funded from the contributions raised from covered employees and their employers at the fixed percentage of wages. At present, covered employees contribute 1.75% of the wages to the ESIC and covered employers contribute 4.75% of the wages, payable to their employees. The state government also contribute 1/8th share cost of medical benefit. ESI Registration is done through LegalStartup.</p>
<h2>Benefits of ESI Registration</h2>
<p>ESI Registration ensures that employees enjoy the following advantages under the Employees’ State</p>
<h5>Medical aid</h5>
<p>From the very first day of employment, registered ESI members and their families are entitled to enjoy the benefits of complete medical care and insurance.</p>
<h5>Maternity aid</h5>
<p>Pregnant women are entitled to maternity benefits that are payable upto twenty six weeks. This period can be extended by 30 days on medical advice. To qualify for maternity benefits, employers are required to contribute their wages for 70 days in the preceding two contributions periods.</p>
<h5>Disablement benefits</h5>
<p>Disabled employees can get 90% of their monthly salaries as disablement benefits.</p>
<h5>Sickness benefits</h5>
<p>Absence from work during illness can be taken for a maximum of 91 days per year along with 70% of the monthly wages.</p>
<h5>Dependent benefits</h5>
<p>In the unfortunate event of the demise of an employee during the employment, the dependents of the deceased will receive 90% of his/her monthly salary.</p>
<h5>Funeral expenses</h5>
<p>The family of the deceased employee is entitled to an additional amount of Rs.10,000 towards funeral expenses.</p>
<h5>Confinement expenses</h5>
<p>In the case of confinement of an insured woman or wife of an employee occurring in a place with no medical facilities under the ESI scheme, confinement expenses can be availed.</p>
<p>In addition, some of the need-based benefits:</p>
<h5>Vocational rehabilitation</h5>
<p>Permanently physically challenged, insured employees are entitled to undergo vocational rehabilitation training at VRS.</p>
<h5>Physical rehabilitation</h5>
<p>Available to employees in the case of physical disablement due to an employment injury or occupational hazards.</p>
<h5>Old age medical care</h5>
<p>An annual payment of Rs.120 ensures medical care benefits for retiring ESI employees or for those who are opting for VRS/ERS. Insured ESI members retiring on account of permanent disabilities are also entitled to the same benefits.</p>
<h5>Extended sickness benefits</h5>
<p>ESI members suffering from chronic diseases can avail the extended sickness benefit for upto 2 years, provided the sick leave of 91 days are expired. Among other conditions, the criteria for eligibility for this benefit include that the concerned ESI member should have been working for at least 2 years at the onset of the sickness and its subsequent diagnosis.</p>
<h5>Enhanced sickness benefit</h5>
<p>This benefit was introduced in 1976 as a measure to encourage ESI employees to undergo vasectomy or tubectomy for family welfare. Registered ESI members willing to undergo the sterilisation procedures are eligible for enhanced sickness benefit of 100% of their daily average wages. This benefit also allows a recuperating period of 7 days for vasectomy and 14 days for tubectomy from the date of surgery or admission in the hospital. Extension of this benefit can also be availed in case of post-operative complications</p>
