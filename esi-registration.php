<h2>What are the documents required for  <b>ESIC Registration</b></h2>
<p>The documents required for the registration are –</p>
<p>1. A registration certificate obtained either under the:</p>
<p>a. Factories Act</p>
<p>b. Shops and Establishment Act</p>
<p>2. Certificate of Registration in case of Company, and Partnership deed in case of a Partnership</p>
<p>3. Memorandum of Association and Articles of Association of the Company</p>
<p>4. A list of all the employees working in the Establishment</p>
<p>5. PAN Card of the Business Entity as well as all the Employees working under the entity</p>
<p>6. The compensation details of all the employees</p>
<p>7. A cancelled cheque of the Bank Account of the Company</p>
<p>8. List of Directors of the Company</p>
<p>9. List of the Shareholders of the Company</p>
<p>10. Register containing the attendance of the employees</p>
<p>After collecting all the above-mentioned documents the following procedure is to be followed for the registration of the ESI:</p>
<p>a. Form No – 1 (Employers Registration Form) is to be downloaded and filled.</p>
<p>b. After downloading the PDF version of the form and filling it, it has to be submitted&nbsp;<a href="http://www.esic.nic.in/" target="_blank" rel="noopener noreferrer">on the website</a>&nbsp;itself along with the above-mentioned documents.</p>
