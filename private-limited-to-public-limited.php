                            <div class="overview_sr " id="6">
                                <h3>
                                    What is the Process of Conversion into a Public Limited Company?
                                </h3>

                                <p>The procedure for conversion into a Public Limited Company in accordance with
                                    the applicable provisions of the Companies Act, 2013 and the Companies
                                    (Incorporation) Rules, 2014 is briefly given below for your understanding:-
                                </p>

                                <ol>
                                    <li>Issue of notice for convening a meeting of the Board of Directors
                                        <p>Notice is to be issued for holding the Board meeting along with the agenda to
                                            be
                                            circulated. The proposed agenda would be:-</p>
                                        <ul>
                                            <li>Pass Board Resolution for Conversion</li>
                                            <li>Fix date, time & venue for conducting of shareholder’s meeting
                                                (Extraordinary General Meeting)</li>
                                            <li>Approve the Notice of EGM & authorized to Company Secretary/Director
                                                for issuance of the notice
                                            </li>
                                        </ul>
                                    </li>
                                    <li>Holding of Board Meeting - Discussing Agenda Items
                                        <ul>
                                            <li>To approve the conversion of Pvt. Limited to Public Limited subject to
                                                the
                                                approval of the shareholders.
                                            </li>
                                            <li>To adopt the new set of Memorandum of Association and Articles of
                                                Association subject to the approval of the shareholders.</li>
                                            <li>Fix the date, time and place of holding the general meeting</li>
                                        </ul>
                                    <li>Issuance of Notice For General Meeting
                                        <ul>
                                            <li>Notice to the members at least 21 days before the meeting
                                            </li>
                                            <li>If consent is given in writing or by electronic means, a general meeting
                                                may be convened by giving a short notice; At least 95% of the members
                                                shall be allowed to vote in such a meeting.
                                            </li>
                                            <li>The notice period can be extended as specified in the articles.
                                            </li>
                                        </ul>
                                    </li>
                                    <li>Conduct The General Meeting
                                        <ul>
                                            <li>To pass a special resolution for the conversion of Pvt. Limited to
                                                Public
                                                Limited
                                            </li>
                                            <li>To adopt the Compliance Memorandum of Association (MOA) and Articles
                                                of Association (AOA).</li>
                                        </ul>
                                    </li>
                                    <li>File Form MGT-14
                                        <p>Special resolution to be filed within 30 days from the date of passage
                                        </p>
                                        <ul>
                                            <li>Attachment with Form MGT 14
                                            </li>
                                            <li>explanatory statement</li>
                                            <li>Notice of EGM</li>
                                            <li>Certified true copy of the special resolution passed at the EGM</li>
                                            <li>AOA Revised
                                            </li>
                                            <li>MoA Revised</li>
                                            <li>If the EGM is held at short notice, consent to short notice.</li>
                                        </ul>
                                    </li>

                                    <li>File Form INC - 27
                                        <p>The special resolution has to be filed within 15 days of its passing.

                                        </p>
                                        <ul>
                                            <li>Attachments to Form INC 27:-
                                            </li>
                                            <li>Certified original copy of the special resolution passed at the EGM
                                            </li>
                                            <li>AOA Revised
                                            </li>
                                            <li> MOA Revised</li>
                                            <li>If the EGM is held at short notice, consent to short notice
                                            </li>
                                            <li> Minutes of the General Meeting of Members</li>
                                        </ul>
                                    </li>
                                    <li>Obtaining the approval of MGT-14 and INC-27:-
                                        <p>The forms will be approved by the RoC, if the RoC is satisfied that the
                                            private
                                            company has complied with the prescribed requirements of the conversion.</p>
                                    </li>
                                    <li>Certificate of Incorporation from ROC
                                        <p>It will issue a fresh Certificate of Incorporation, once ROC has approved the
                                            forms MGT 14 and INC 27, & shall cancel former registration of the company.
                                        </p>
                                    </li>
                                </ol </div>


