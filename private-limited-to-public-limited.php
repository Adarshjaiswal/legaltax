                            <div class="overview_sr " id="2">
                                <h3>
                                    The Requirements to Convert Private Limited to
                                    Public Limited
                                </h3>
                                A Private company can be converted in to public company by complying with the
                                following requirements, which are mentioned below:
                                <ul>
                                    <li>As per Section 14 by passing a special resolution, alteration of its articles
                                        by AOA, thereby deleting the three restrictions of a Pvt. Company.
                                    </li>
                                    <li>As per Section 13 by passing special resolution, altering its name thereby
                                        removing the word ‘Private’ from its name.</li>
                                    <li>If there are only 2 directors then the number of directors should be
                                        increased to at least 3, for these steps should be taken to increase the
                                        number of members to at least 7 if the number of members is below 7.</li>
                                    <li>Under Section 14 from the date of passing Special resolution the company
                                        turned out to be public. However, the change in its name, by deleting the
                                        word ‘Private’ becomes in effect only on issue of fresh incorporation
                                        certificate by the ROC.
                                    </li>
                                </ul>
                            </div>


