                <h3>
                  Trademark Opposition in India
                </h3>
                <p>
                  Section 21 of the Trademarks Act,1999 states that “any person” can file the notice of opposition, including any natural or legal person such as a corporation, partnership, etc., belonging to an Indian or overseas customer, member of the public or a competitor or other person.
                </p>
                <p>
                  When 2 or more people have the same problems against a brand, they can even merge together and oppose the same.
                </p>
                <p>
                  In general, the following individuals raise a trademark contradiction:
                </p>
                <ol>
                  <li>
                    <b>- Already Registered- </b> The owner of a previous trademark application or registration of a similar product.
                  </li>
                  <li>
                    <b>- Prior User- </b> A person who has used the same trademark or a similar trademark before the customer has not registered his trademark.
                  </li>
                </ol>
                <p>
                  The reasons and grounds available for filing a trademark opposition in India depend on the facts and the circle. Circumstances of each of the cases, however, some of the main reasons for opposing a trademark under the Trademark Act 1999 are mentioned above in this article.
                </p>

                <h4>
                  <b>
                    The procedure for trademark opposition in India
                  </b>
                </h4>
                <ul>
                  <li>
                    <p>
                      <b>Notice of Opposition - </b>
                      Any person can file an opposition against a trademark that appears in the trademark journal within 4 months from the date of first publication. The same office or branch office where the applicant submitted the original application for trademark registration. The headquarters of the trademark register is in Mumbai, and the offices are in Ahmedabad, Chennai, Delhi, and Kolkata.
                    </p>
                  </li>

                  <li>
                    <p>
                      <b>Counter-Statement - </b>
                      Rule 44 of the Trademark Act,2017, stipulates that the application for the registration of the trademark concerned can submit a counter-declaration within two months (non-renewable) after receiving the TM-O form after submitting the notice of opposition. <br><br>
                      If the counter claim is not filed, the registration application which the applicant will be deemed abandoned, and the Registrar will remove it from the journal and disagree.

                    </p>
                  </li>

                  <li>
                    <p>
                      <b>Evidence supporting the opposition - </b>
                      Rule 45 of the Trademark Rules, 2017 states that the opponent must provide evidence in support of their opposition within 2 months (renewable by one month) of receiving the copy of the counter claim filed by the attorney. The party also has the option of refraining from submitting evidence and relying solely on the notice of opposition.
                    </p>
                  </li>

                  <li>
                    <p>
                      <b>Evidence supporting the application - </b>
                      After the opponent has submitted evidence in support of its objection, the applicant also has 2 months to produce evidence in your favor. You also have the option not to present evidence and rely solely on the counter claim, as provided in Rule 46, Trademarks Act,2017.
                    </p>
                  </li>

                  <li>
                    <p>
                      <b>Response to Evidence - </b>
                      Under Rule 47, Trademarks Act,2017, the opponent is given an extra month (extendable for 1 month) to provide evidence in response to the applicant’s evidence. <br><br>
                      At the end of this phase, the parties can no longer provide any further evidence. On request, additional cost and terms at your own discretion. For this, the interested party must submit an interim application.

                    </p>
                  </li>

                  <li>
                    <p>
                      <b>Hearing - </b>
                      Once the Evidence phase is complete, the Registrar will notify the parties, the date of hearing. The hearing is based on the notice of opposition, the reply, and the evidence submitted. The parties are obliged to inform the Chancellor of their intention to appear at the hearing. The hearing can also be postponed, and the request can be submitted in the prescribed form.
                    </p>
                  </li>

                  <li>
                    <p>
                      <b>In the event of a complaint - </b>
                      The Registrar, by hearing both parties and examining the evidence presented, decides whether the opposition is successful or not, and in turn, decides whether the trademark is registered or not the party complained. The party affronted by the Registrar’s decision can challenge it by filing an appeal with the Board of Appeal for Intellectual Property.
                    </p>
                    <ul>
                      <li>
                        <p>
                          The registration process should be followed correctly. Otherwise, your trademark will be objected by the Registrar. If your trademark is being used by an authorized individual without any acceptance, then you can file an opposition against it and can seek legal remedy under The Trademarks Act, 1999 and 2017, respectively.
                        </p>
                      </li>
                    </ul>
                  </li>

                </ul>
