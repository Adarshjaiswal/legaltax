                <h3>
                  TRADEMARK INFRINGEMENT IN INDIA 
                </h3>
                <p>
                  Trademark infringement is a violation of the exclusive rights attaching to a registered trademark without the authorization of the trademark owner or any licenses (provided that such authorization was within the scope of the license). Infringement may occur when the infringer (i.e. the person doing the infringing act), uses a trademark which is identical or confusingly similar to a registered trademark owned by another person, in relation to products or services which are identical or similar to the products or services which the registration covers. The owner of such registered trademark may commence legal proceedings against the infringer.
                  <br><br>
                  A trademark which is not registered cannot be infringed as such, and the trademark owner cannot bring infringement proceedings. Instead, the owner can commence proceedings under the common law for passing off or misrepresentation, or under legislation which prohibits unfair business practices. In some jurisdictions, infringement of trade dress may also be actionable. To establish infringement with regard to a registered trademark, it is necessary only to establish that the infringing mark is identical or deceptively similar to the registered mark and no further proof is required.
Trademark infringement is an infringement of exclusive rights attaching to a trademark without the authorization of the trademark owner or nay license. Trademark infringement typically occurs when a person uses a trademark which may be either a symbol or a design, with the resembles to the products owned by the other party. The trademark owner may begin an officially permitted proceeding against a party, which infringes its registration. There are two types of remedies are available to the owner of a trademark for unauthorized use of its imitation by a third party. These remedies are an action for passing off in the case of an unregistered trademark and an action for infringement case of a registered trademark. An infringement action and an action for passing off are quite different from each other, an infringement action is a statutory remedy and an action for passing off is a common law remedy.
In case of infringement/passing off trademark, a criminal complaint can also be filed. It may be noted that under the Provisions f the Trade Marks Act, 1999, the offences under the Act are Cognizable, meaning there by the police can register an FIR (First Information Report) and prosecute the offenders directly.


