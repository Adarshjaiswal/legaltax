                <h3>
                  Trademark Assignment
                </h3>

                <p>
                  Trademark is an intellectual property and like any other asset, the proprietor of a trademarks has the authority or power to sell, license or transfer the owned intellectual property. Such transfer can be made through Trademark Assignment or through licensing. <br><br>
                  Whenever a trademark is assigned, there is some sort of variation in the ownership of the registered brand. But when it is licensed, the benefits related to the trademark continue to vest with the buyer but only a few qualified rights are given to the third party. This assignment can be done with or without assigning the goodwill of business. In case of a registered trademark, such as assignment is required to be recorded in the Register of a trademark. <br><br>
                  Some of the advantages of the trademark assignment (concerning both the owner and the buyer) are listed hereunder-

                </p>



                <ul>
                  <li>
                    <p>
                       The trademark assignment enables the owner of the trademark to en cash the value of his brand.
                    </p>
                  </li>

                  <li>
                    <p>
                       With the help of a trademark assignment, the assignee can obtain the rights of an already established brand.
                    </p>
                  </li>

                  <li>
                    <p>
                       The assignment of the trademark supports both the assignor and the assignee to expand their respective business.
                    </p>
                  </li>

                  <li>
                    <p>
                       In case of any dispute, the trademark assignment agreement would enable the assignor or the assignee to establish the legal right.
                    </p>
                  </li>
