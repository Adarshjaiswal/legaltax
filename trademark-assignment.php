                <h3>
                  Types of Trademark Assignment:
                </h3>
                <p>
                  There are four types of trademark assignments-
                </p>
                <ol>
                  <li>
                    <p>
                      Partial assignment,
                    </p>
                  </li>
                  <li>
                    <p>
                      Complete assignment,
                    </p>
                  </li>
                  <li>
                    <p>
                      An assignment with Goodwill, and
                    </p>
                  </li>
                  <li>
                    <p>
                      An assignment without Goodwill / Gross assignment.
                    </p>
                  </li>
                </ol>


                <p>
                  <b>
                    All the four types of trademark assignments are briefly explained here as follows-
                  </b>
                </p>


                <p>
                  <b>
                    1. Partial assignment:
                  </b>
                  Under the partial assignment, the assignor transfers only limited ownership with regard to specific products/ services.
                </p>


                <p>
                  <b>
                    2. Complete assignment:
                  </b>

                  Under the complete assignment, entire rights with respect to the registered trademark are transferred by the assignor to the assignee.
                </p>


                <p>
                  <b>
                    3. An assignment with Goodwill:
                  </b>
                  Where an assignor assigns to the assignee, the value, rights and entitlements also, as associated with a trademark with respect to the goods or services already in use by the assignor. After taking over the goodwill associated with the trademark, the assignee is free to use the trademark assigned to him for all goods or services including for the goods or services which were already in use by the Assignor. Such assignment is called assignment with Goodwill of Business.

                  <br>
                  <b>For Example: </b>
                  Mr. A is the owner of a trademark “TM” who is already using the said trademark “TM” in relation to clothing and footwear. Mr. A assign to Mr. B the said trademark “TM” through an agreement (in writing) in relation to clothing and footwear along with the Goodwill associated with trademark “TM”. <br><br>
                  In this case, Mr. A has also assigned to Mr. B, the Goodwill associated with trademark “TM” for the business of clothing and footwear as well as for other goods or services. Therefore, Mr. B is eligible to use the said trademark “TM”, for clothing and footwear including other goods or service dealt by Mr. B.

                </p>

                <p>
                  <b>4. Gross assignment or Assignment without Goodwill: </b>
                  assignment, while transferring the trademark, the assignor will restrict the buyer’s right. The assignor here restricts the buyer from using a brand of the product which is already being used by the assignor. In nut-shell, the goodwill attached to the brand is not transferred to the assignee. <br><br>
                  Where an assignor assigns to the assignee, the right and entitlements in a trademark with respect to the goods or services which are not in use by the assignor. In other words, where the assignor restricts the assignee with a condition that the assignee is not entitled to use the trademark assigned in relation to the goods or services already in use by the assignor. Such assignment is called assignment without the Goodwill of Business. <br><br>
                  <b>For Example:</b> Mr. A is the owner of a trademark “TM” who is already using the said trademark “TM” in relation to clothing and footwear. Mr. A assign to Mr. B the said trademark “TM” through an agreement (in writing) in relation to goods or services other than clothing and footwear without assigning the Goodwill associated with trademark “TM”.<br><br>
                  In this case, Mr. X has not assigned to Mr. B, the Goodwill associated with trademark “TM” for the business of clothing and footwear. Therefore, Mr. B is not eligible to use the said trademark “TM”, for clothing and footwear. Thus, in case, Mr. B wishes to use the said trademark “TM” in relation to other goods or services then he will be required to create separate Goodwill for trademark “TM” for such other goods or services dealt by him.

                </p>



