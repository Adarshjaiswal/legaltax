                <h3>
                  Time Limit for Trademark Opposition:
                </h3>
                <p>
                  After the trademark advertisement in the <b>Trade Mark Journal,</b> any person can oppose the trademark registration for <b>three months</b> (which may be extended by period not exceeding one month). Trademark opposition filings can be done only at the Trademark Registrar’s office and cannot be taken directly to a Court or the Appellate Board (IPAB). <br><br>
                  If a trademark opposition is successful, the registration of the trademark will be refused. If the trademark opposition application is rejected, the brand will be registered.

