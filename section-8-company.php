                <h3>
                  About Section 8 Company
                </h3>
                <p>
                  Registering your NGO as a section 8 company has many perks compared to registering as a trust or a society. A Section 8 Company is a specific form of Company which is incorporated for the promotion of commerce, art, science, education, research, sports, charity, social welfare, religion, protection of environment or any such other object. It intends to apply all its profits, income, or other earnings, in promoting these objects. Pays no dividend or income to its members. These are limited companies, which are registered under the Companies Act, and will be treated as limited companies without the phrase “limited” being added to their name. They may have been registered either as “private limited or public limited companies”.
                  Section 25 Company (under Companies Act, 1956) is a prior version. Section 8 Companies are a legal form of “Non-Profit Organizations (NPOs) or Non-Governmental organizations (NGOs)”. A Section 8 Company has the authority to work anywhere in the country.


