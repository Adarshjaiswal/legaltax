                <h3>
                  Other than the conditions laid down in it’s definition above, the following criteria must be fulfilled as well for registering a Section 8 Company:
                </h3>

                <ul>
                  <li>
                    <p>
                      <b>  Registration Under: </b>
                      Companies Act, 2013.
                    </p>
                  </li>

                  <li>
                    <p>
                      <b>  License: </b>
                      License to be applied to MCA.
                    </p>
                  </li>

                  <li>
                    <p>
                      <b>  Directors: </b>]
                      A Minimum of “2 Directors for a Private Limited Company and 3 Directors for a Public Limited Company” must be there.
                    </p>
                  </li>

                  <li>
                    <p>
                      <b>  Indian Resident: </b>
                      At least 1 director must be a resident of India, i.e., have stayed in India for a total period of not less than 182 days in the previous calendar year [(Section 149(3)].
                    </p>
                  </li>

                  <li>
                    <p>
                      <b>  Subscribers to MoA: </b>
                      If the Company is proposed to be incorporated as a private company or a public company respectively, it’s MoA must have at least 2 or 3 subscribers.
                    </p>
                  </li>

                  <li>
                    <p>
                      <b> MoA&AoA: </b>
                      Decide about the name to be applied for, objects to be carried by the Company, planned registered office address, number of Directors and promoters, authorized capital, and number of shares to be subscribed by each promoter. They must mention the plan laid-out to meet your social objectives. The ROC (Registrar Of Company) is entitled to ask about it.
                    </p>
                  </li>

                  <li>
                    <p>
                      <b>  Initial Capital: </b>
                      Whatever amount of initial capital has been proposed for the Company, it must get invested in the Company within 2 months.
                    </p>
                  </li>

                  <li>
                    <p>
                      <b>  Property Management: </b>
                      : The ownership of the property lies in the name of the Company and it can only be sold as per the rules mentioned under the Companies Act. (Ex: With the consent of the Board of Directors in the form of a resolution).
                    </p>
                  </li>

                  <li>
                    <p>
                      <b>  Dissolution: </b>
                      The Section 8 Company may be wind-up only by following the bye-laws of the society. Upon dissolution and after settling all debts and liabilities, the funds and property of the society are not to be distributed among the members of the company. Instead, the remaining funds and property would be given or transferred to some other Section 8 Company, someone that has a similar object.
                    </p>
                  </li>
                  <li>
                    <p>
                      <b> Annual Compliance: </b>
                      There must be Annual filing of accounts, statements and returns of the company with the ROC, in order to adhere to the set compliances.
                    </p>
                  </li>

                  <li>
                    <p>
                      <b> Documents: </b>
                      All the Directors must have their valid DIN (Director’s Identification Number) & DSC (Digital Signature Certificate).
                    </p>
                  </li>
                </ul>



