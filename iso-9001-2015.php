                  <a href="https://en.wikipedia.org/wiki/ISO_9000" style="color:blue"> ISO 9001:2015</a> Clauses
                </h3>
                <h3>Following Are Some Important Clauses Of ISO 9001 2015:</h3>
                <ul>
                  <li>
                    <p>
                      <b>Clause 1: Scope - </b>
                      The Section defines the scope of the scope of this standard includes specifying the requirements for the quality management system of any organization
                    </p>
                  </li>

                  <li>
                    <p>
                      <b>Clause 2: Normative references - </b>
                      The supporting standard referred to in this standard referred to in this standard mandatory for its application is ISO 9001:2015 which covers fundamentals & terminology
                    </p>
                  </li>
                  <li>
                    <p>
                      <b>Clause 3: Terms & Definitions - </b>
                      The terminology used throughout this standard comes directly from ISO 9000:2015, QMS - Terminology and Fundamentals
                    </p>
                  </li>
                  <li>
                    <p>
                      <b>Clause 4: Context Of the Organization - </b>
                      When you are implementing a quality manual system, the first step is to align your business objectives and intent with those of the QMS. Know external and internal issues, needs and expectations of interested parties, QMS scope and its processes.
                    </p>
                  </li>

                  <li>
                    <p>
                      <b>Clause 5: Leadership - </b>
                      The section covers leadership responsibilities. Top management must demonstrate, establish and communicate a quality policy and ensure that responsibilities and authorities are communicated, assigned and understood.
                    </p>
                  </li>

                  <li>
                    <p>
                      <b>Clause 6: Planning for the QMS - </b>
                      This clause covers organizational OMS planning to address organizational risks, changes, opportunities and quality objectives.
                    </p>
                  </li>

                  <li>
                    <p>
                      <b>Clause 7: Support - </b>
                      This clause of ISO 9001 2105 covers the resources required for a quality management system, such as enabling and aware employees, providing resources, and including documented descriptions to support your QMS.
                    </p>
                  </li>

                  <li>
                    <p>
                      <b>Clause 8: operation - </b>
                      This clause covers the control and planning processes necessary to meet the needs of goods and services (such as production and service provision, design and development, release of goods and services, external providers, etc.).
                    </p>
                  </li>

                  <li>
                    <p>
                      <b>Clause 9: Performance Evaluation Clause - </b>
                      The ISO 9001 needs your organization's Quality Management System to measure, analyze, monitor and evaluate your QMS.
                    </p>
                  </li>
                  <li>
                    <p>
                      <b>Clause 10: Improvement - </b>
                      This clause of ISO 9001 2105 covers continuous improvement Select opportunities for improvement, take action against nonconformities, take corrective action as needed and continually improve your quality management system.
                    </p>
                  </li>
                </ul>
