                  <a href="https://www.iso.org/home.html" style="color:blue">ISO</a> 9001 2015 does not dictate what the objectives of a company or organization should be or how to achieve them. In simple words, it does not tell a company or organization how to run its business, This standard defines control principles that can be used to create efficiencies by integrating and aligning processes throughout the organization, meeting regulatory requirements and helping organizations expand into new markets where customer ISO demands certification.
