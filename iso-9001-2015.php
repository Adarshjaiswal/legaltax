                <h3>
                  An Overview of ISO 9001 2015
                </h3>
                <p>
                  ISO 9001 2015 is an international standard for maintaining, creating and implementing a<b> Quality Management System (QMS)</b>. ISO 9001 2015 was first introduced in september 2015 and is already the most widely adopted standard in the history standards. It draws a framework for improving the quality and understanding of language for any company or organization to constantly provide goods and services to meet the expectations and needs of customers and other applicable interested parties in the most effective manner. A quality management system is the set of all resources, cultural values, processes and assets that support the goal of customer satisfaction and organizational efficiency.

