              <div class="overview_sr " id="3">
                <h3>
                  One Person Company

                </h3>
                <p>A One Person Company is a company that has one person as its members. A member
                  of a company is a subscriber to its Memorandum of Association. So, a one person
                  company is a company which has only one shareholder as its member. Generally, OPC
                  registration is done when there is only one member or promoter for the business. In the
                  initial stages, the businessman prefers OPC registration instead of a sole proprietorship
                  business because of the many benefits that OPC provides.</p>
                <b>
                  <p>What are the main features of OPC registration?
                </b></p>
                <ol>
                  <b>
                    <p style="color: hsl(32, 60%, 49%)" ;>The Key features of One Person Company are :
                    </p>
                  </b>
                  <li> OPC Registration - The registration of OPC is done as per the Companies
                    Act 2013 and Ministry of Corporate Affairs.
                  </li>
                  <li>OPC is separate from its members - OPC is treated as a separate legal
                    entity.</li>
                  <li>Liability of members - The liability of the member is limited to the extent of
                    his share capital.
                  </li>
                  <li> Criteria for minimum and maximum number of member/s - minimum 1
                    member and maximum 2 members.</li>
                  <li>Transferability - Transferable to 1 person only.
                  </li>
                </ol>
                
              </div>
              <div class="overview_sr " id="4">
                <h3>
                  Eligibility Criteria for One Person Company Registration

                </h3>
                <b>
                  A person (natural person) who is a resident of India in the previous calendar year
                  can apply for OPC registration.
                </b>

                <p style="font-style: italic" ;>Note - Resident means who has lived in India for 182 days in
                  the previous
                  calendar year.
                </p>
                <ul type="1">

                  <b>
                    <li> A person cannot include more than one OPC or be a nominee of more
                      than one OPC.
                    </li>
                  </b>
                  <p>For OPC registration, the <b>paid-up capital limit is Rs 50 lakh and the
                      average annual turnover is Rs 2 crore</b> in the immediate preceding
                    financial year. The state of the OPC is lost, if the OPC exceeds this limit.

                  </p>


                  <b>
                    <li>
                      1 member can form an OPC.

                    </li>
                  </b>

                  <li>
                    <p>OPC registration rules do not allow non-banking financial institutions.
                    </p>
                  </li>
                </ul>
              </div>


