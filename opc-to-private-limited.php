                <h3>
                  Conversion Process of One-person Company to
                  Private Limited Company</h3>
                <b>
                  <p style="color: hsl(32, 60%, 49%)" ;>Now in this topic, we are taking an in-depth look at the
                    conversion
                    process of OPC into private company and public company-</p>
                </b>
                <ol type="1">
                  <p>Conversion of One Person Company can be done by either-</p>
                  <li>voluntarily or,
                  </li>
                  <li> Mandatory.
                  </li>
                </ol>
                



                <b>
                  <p style="color: hsl(32, 60%, 49%)" ;>For voluntary conversion, the OPC shall satisfy the following
                    criteria-
                  </p>
                </b>
                <ol type="1">

                  <li>Must have completed at least 2 years from the date of OPC registration.

                  </li>
                  <li> Application in Form INC-6
                  </li>
                  <li>The limit of shareholders and directors will have to be increased to the
                    required limit.
                  </li>
                </ol>




                <b>
                  <p style="color: hsl(32, 60%, 49%)" ;>For mandatory conversion, OPC shall meet the following criteria-
                  </p>
                </b>
                <ol type="1">
                  <p>The OPC has to compulsorily convert itself into a private or public company, if-
                  </p>
                  <li>The OPC has to compulsorily convert itself into a private or public company, if-

                  </li>
                  <li> Notice of Conversion to Form INC-5
                  </li>
                  <li>Filing Form INC-6.</li>
                  <li>OPC will have to increase the required limit of directors and shareholders.</li>
                  <li>Further, if the OPC exceeds the limit prescribed as a statutory body, it
                    needs to be converted into a private or public company and inform the
                    ROC by filing Form INC-5 for the same OPC.</li>

                </ol>
                <p style="font-style: italic" ;>Note - INC-5 will be filed within 60 days of exceeding the limit. An OPC
                  shall file Form INC-6 for the conversion of an OPC into a private or
                  public company</p>

                <b>
                  <p>Point to remember- In case of conversion of private company or
                    public company into OPC, the same Form INC-6 will be filed by private
                    company or public company for conversion of itself into OPC.
                  </p>
                </b>
                <ul>
                  <p>The process to convert OPC into private or public company is mentioned below-
                  </p>
                  <li> The company shall give notice to the directors with the agenda at least
                    seven days prior to the date of the meeting for convening a meeting of the
                    Board.
                  </li>
                  <p style="font-style: italic" ;>Note - For consideration of the conversion, notice will be given in
                    compliance with Secretarial Standard I. </p>

                  <li> To convene a meeting of the Board to decide the day, time, date and place
                    for convening an extraordinary general meeting</li>
                  <li>Approval of requisite documents (Draft Notice, Draft MOA and AOA) before
                    convening the Extraordinary General Meeting.
                  </li>
                  <li>Providing authority to ensure compliance.</li>
                  <li>The resolution passed in the meeting shall be passed to the members and
                    shall be recorded in the minutes book of the company.
                  </li>
                  <p style="font-style: italic" ;>Note- This shall be signed and dated by the member.</p>
                  <li>The Company (OPC) shall file Form MGT-14 to the ROC within 30 days
                    from the date of passing the resolution along with the required documents-</li>
                  <li>Attested true copy of Board's resolution, minutes and latest financial
                    statements.</li>
                  <p style="font-style: italic" ;>Note- Once MGT-14 is approved, only INC-5 will be filed for conversion
                    by
                    the company</p>
                </ul>



