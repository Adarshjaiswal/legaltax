                <h3>
                  OPC Registration Process

                </h3>

                <ol type="1">

                  <p style="color: hsl(32, 60%, 49%)" ;>Here is brief of One Person Company Registration process -
                  </p>
                  <li> For OPC registration the applicant is required to obtain a DSC (Digital
                    Signature Certificate) issued by the Certifying Authority, as the authenticity of all
                    the documents is ensured by the DSC.
                  </li>
                  <li>Registration of OPC through RUN (Name Reservation) -
                    <ul>
                      <li> RUN service will be used for proposed company name availability</li>
                      <li> Once the name is approved, SPICE form will be filed for the company i.e.
                        (OPC should be incorporated within 20 days from the date of approval of
                        RUN).
                      </li>
                      <li> If the correspondence address and the registered office address are not
                        the same, the company shall file Form INC-22 within 30 days from the date
                        Spice is registered and incorporated.
                      </li>
                      <li> Allotment of DIN (Director Identification Number).</li>
                      <li> Application of PAN and TAN while doing OPC registration through Spice
                        Form INC-32.</li>
                      <li>Electronic MOA and AOA through SPICE Forms (INC-33 and INC-34).</li>
                      <li> A declaration regarding incorporation by an Advocate/CA/CWA/CS stating
                        that all the legal requirements of this Act have been duly complied with. A
                        declaration is given by the professionals involved in the OPC registration
                        process.</li>
                      <li> Nominee has consent that he is desirous of becoming a member of the
                        One Person Company.
                      </li>

                    </ul>
                  </li>

                </ol>
                
