                  The demand for security has increased considerably due to an increase in the number of business establishments in recent years. The growing tendency of hiring security guards from private sources by the private establishments, firms, person or property, etc. has led to the coming up of a large number of private security agencies across the country. Although these private security agencies help meeting the security requirements of the private sector, on the other hand, there also is a concern for the way these agencies across the country. Although these private security agencies help meeting the security requirements of the private sector, on the other hand, there also is a concern for the way these agencies work. Therefore, the Private Security Agencies Regulation Act laid numerous sets of rules for the proper functioning and registration of the private security agencies. <br><br>

                  PSARA License is a prerequisite before any person starts a private security business in India. The Private Security Agency means an entity that provides Private Security Guards and other related services at an establishment as an alternate to the police. The private security agencies’ operation is governed by The Private Security Agencies Regulation Act, 2005, enforced by the Department of Internal Security of the Ministry of Home affairs, Government of India <br><br>
                  After the enactment of the PSARA (Act), it is mandatory for a security agency to obtain a Licence from the State Controlling Authority Concerned prior to commencing the business and in case of non-compliance, the law provides for stringent penal consequences. The application for PSARA License to start a private security agency is made to the competent authority of the state. The PSARA License is issued to operate in one or more districts of a particular state or for the entire state.


